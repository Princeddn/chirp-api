function readWeekdays(data) {
    var weekdays_map = { monday: 1, tuesday: 2, wednesday: 3, thursday: 4, friday: 5, saturday: 6, sunday: 7 };

    var weekdays = {};
    for (var key in weekdays_map) {
        weekdays[key] = readEnableStatus((data >> weekdays_map[key]) & 0x01);
    }
    return weekdays;
}

